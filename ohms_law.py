# Ohm's Law Calculator: V = I * R

# Get user input for current (I) and resistance (R)
I = float(input("Enter current (in amperes): "))
R = float(input("Enter resistance (in ohms): "))

# Calculate voltage
V = I * R

# Display the result
print(f"Voltage (V) = {V} volts")


